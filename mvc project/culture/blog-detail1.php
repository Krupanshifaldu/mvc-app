						<h3>IS FABRIC PAINT THE SAME AS ACRYLIC?</h3>
						<p>Fabric Paint and Acrylic paint actually have a lot in common – it’s basically just the binding agents that make them different. Acrylic paints use a binder too, but it’s a more brittle version than fabric paints.

That makes a lot of sense, since many tutorials for painting fabric online use a regular acrylic paint and a textile medium (that acts as the binder). Of course, it likely won’t work as well as something formulated specifically to be used on fabric, but it opens up a huge array of colors – acrylic paint comes in basically every hue you can think of.</p>
<h3>WHAT’S THE DIFFERENCE BETWEEN FABRIC PAINT, DYE, AND INK?</h3>
<p>There are a TON of products on the market designed to help you change the color of fabric. In short, dye soaks into the fabric, while paint tends to sit on top…but there’s more to the answer than that.Fabric Dye changes the color of your fabric without changing the texture by combining dye with mordants to help them adhere to the fabric. Nearly all dyes these days are synthetic, but you can definitely still purchase plant, animal, or mineral-based dyes.</p>

<p>Fabric Ink, on the other hand, changes the color of your fabric while only very slightly changing the texture (depending on the type of ink). Sometimes, fabric inks are alcohol-based, and other times they’re acrylic-based. I like to think of them as the spectrum between a true dye and a true paint.</p>

<p>Fabric Paint, which is usually acrylic paint mixed with a bonding agent, usually changes the texture of your fabric – though it really depends on the formulation and amount applied.</p>	
