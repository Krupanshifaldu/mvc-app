						<center><h1 style="font-size:40px;"><b>Fabric Painting Techniques</b></h1></center>
						<p>Fabric painting simply refers to any painting done on a fabric. Today, people use fabric painting techniques to paint apparel such as t–shirts and jeans as well has home furnishings. However, these techniques have been around for thousands of years in Asia. It is believed that fabric painting originated around the the same time as weaving.</p>

<p>When Alexander the Great invaded India, many painted and printed fabrics were found. These fabrics made their way around trade routes as people traded these beautiful fabrics for other goods.

There is a rich history of fabric painting in Africa, Asia, and Europe as different cultures introduced new techniques to different areas.

There are many different techniques a person may use to paint fabric. Designs can be subtle or bold. Many art museums have samples of painted fabric throughout the centuries.

</p>
<h3>How to Paint Fabric: First Steps</h3>
<p>How do you paint fabric? Before painting fabric, a person needs to choose the fabric to be used. As will be discussed below, there are multiple different types of fabric that may be used.</p>

<p>Before beginning painting, a person would need to wash the fabric. Fabric weavers may use multiple different types of chemicals on the fabric, and these need to be washed away so that the paint will stick to the fabric. Washing the fabric also serves the purpose of removing the sizing, which is a coating added to every thread before weaving to improve weight, stiffness, and shine. It can be made of multiple different materials that make the fabric look more appealing.</p>

<p>Once the fabric is washed, it should be laid flat. It is not important to iron the fabric unless needed. When painting something like a t–shirt that has a front and a back, it is important to put cardboard or another material between the two pieces of fabric so the paint does not bleed from the front to the back or vice versa.</p>
<h3>What is Fabric Paint?</h3>
<p>There are multiple different choices when it comes to the paint to choose. Many craft and art stores sell fabric paint. What is fabric paint? This refers to paint and markers specifically made for use on fabrics. With these fabric paints, the artist does not have to worry about whether or not the paint will adhere to the fabric.</p>

<p>If an artist does not want to use fabric paint or if there is none available, he or she may choose to use acrylic paint with the aid of an extender medium. An extender medium is a clear substance that must be mixed with acrylic paint in order for the paint to adhere to the fabric.</p>
<h3>How to Use Fabric Paint</h3>
<p>How do you use fabric paint? There are multiple techniques and supplies an artist might employ when painting fabric. One of these supplies is a brush. There are many different types and sizes of brushes available. The type and size of brush used will depend on the artist’s purpose and the paints and fabrics used. These aren’t always necessary, however, if the artist chooses to use a fabric pen or marker.</p>

<p>There are multiple different types of fabrics that may be used, including cotton, silk or linen. Others may also be chosen depending upon the need. The fabric chosen will depend on the type of painting desired as well as the purpose for the final piece. Silk is used when choosing serti painting, while linen or cotton may be used for clothing or home decor.</p>
                       
