										<p>This is something I was definitely not expecting during my tests – just how different each of the textures of the finished fabric paints were. They ranged from not changing the fabric’s feel at all to feeling completely plastic-coated.</p>

<p>If you were to use these fabric paints on a non-cushy item, I think some of the more plastic-y paints could work well, but I could never imagine sitting comfortably on an item coated in that texture.

Overall, if you’re going to be using fabric paint on upholstery, I’d stay away from anything with the word ‘dimensional’, ‘3-D’, etc., and instead go for one of the more ink-like budget-friendly options, or a higher-end option like Jacquard’s products.</p>
