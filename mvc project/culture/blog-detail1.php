                        				<h6><a href="404.php">A NOTE ABOUT NATURAL FIBERS AND SYNTHETICS</a></h6>
										<p>Not all fabric paints are well-suited for both natural fibers and synthetics. Nearly all-natural fibers (cotton, wool, silk, etc) are pretty easy to dye, but synthetics tend to be a lot harder. You’ve probably experienced this if you’ve ever tie-dyed a t-shirt, and while the cotton of the shirt changed colors, some of the synthetic thread in the stitching stayed white.</p>

<p>Because there are so many different kinds of fabric, you’ll want to test your paint of choice in a small, inconspicuous area before you start the whole project to make sure things adhere well.</p>
