							<p>What is fabric paint made of? Fabric paint has always been so interesting to me – but is it actually paint? Is it really ink? And what is it made of? I recently acquired some lovely wingback chairs that a neighbor was getting rid of, so I thought it would be best to find out what fabric paint is made of, and if it differs from brand to brand. That way I can get the best result for my particular type of fabric and project.</P>

<p>Fabric paint is a dye-like paint that has binding agents added to increase its flexibility and durability. Depending on the desired opacity, it can be thicker or thinner than acrylic paint.</p>
<p>But there’s actually a lot more to it than that – below I’ll cover what makes fabric paint unique, whether or not you have to set fabric paint with heat, and if fabric paint and acrylic paint are the same. I’ll also cover the differences I noticed between testing all of these paints on a few different materials. Also, though I’ll be mentioning a lot of brands in this post, I’m not being sponsored to say anything and all opinions are my own.</p>
							
							<h3>WHAT MAKES FABRIC PAINT UNIQUE</h3>
							<p>Most fabric paint companies are surprisingly secretive about the ingredients of their paints – I called a ton of different manufacturers and none of them would share even the basic details of ingredients in their product</p>
							<ul>
                              <li>Fabric paint can be either thinner or thicker than acrylic paint, depending on the desired opacity</li>
							  <li>The rheology of the fabric paint can also differ based on it’s indended usage (think of rheology as a more in depth version of viscosity that works better for soft-solids)</li>
							  <li>There are essentially two types of fabric paint – those that stay on top of the fabric, and those that penetrate into the fibers. The fabric that stays on top gives an opaque finish, and those that penetrate into the fibers gives a more translucent finish</li>
							  <li>If you’re looking for a super soft finish, you’ll want to go with a penetrating, translucent option (like Jacquard’s Dye-Na-Flow Line)</li>
							  <li>If you’re looking for a super opaque finish, you’ll want to go with a fabric paint that sits on top of the fibers (Like Jacquard’s Neopaque Line)</li>
							 
