<h3>FABRIC PAINTS AVAILABLE AT MICHAELS – IMPRESSIONS</h3>
<p>Because all the brands (besides Jacquard) were so secretive about what makes a fabric paint different from acrylic or latex paint, I decided to test things out for myself. I made a stop at Michaels to pick up everything but the Jacquard products, and to my local professional art supply store for the Jacquard products.</p>

<p>I noticed that anything with the word ‘ink’ in it seemed to soak through the fibers really well, but they also had a more translucent coloration. I can’t confirm (since most companies wouldn’t talk to me about specifics), but I would guess there is probably a higher amount of alcohol in the ink-like paints.</p>

<p>Anything with the word ‘dimensional’ or ‘3-D’ seemed to sit on top of the fibers and was MUCH thicker, which is great if you’re trying to cover up any texture. The two thickest were the Fabric Creations Plush 3-D Fabric Paint and the Tulip Puffy Dimensional Fabric Paint.</p>
<h3>JACQUARD FABRIC PAINT – IMPRESSIONS</h3>
<p>And then there were the Jacquard paints – sometimes making the extra effort to pick up a professional product is really worth it, and that’s exactly what happened in this case.

If I was going for an ink-like wash of color, I would absolutely use the Dye-Na-Flow. It acted exactly as ink would, and spread very easily.

