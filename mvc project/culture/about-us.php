							           <h2>ABOUT OUR ART - FEBRIC PAINT </h2>
									   <H3>What is Fabric Painting?</H3>
							           <p>Fabric painting as the name suggests is a painting that is done on cloth.  This painting can be done on a variety of fabrics from cotton, silk, linen to nylon and velvet.  In fabric painting, we usually use acrylic fabric colors to paint on the cloth. Within a few minutes to hours we can create interesting and beautiful designs on dresses, bedsheets, jackets, sarees, etc.  Any design of can be painted on fabric from flowers, animals, birds, border designs or motifs.  Both traditional to modern designs can be used depending on the style of the garment.</p>
									   <P>One of the most common styles of fabric painting is using a paintbrush to apply paint to a fabric just as one might do on paper. The dried painting can then be worn. Some people find that stenciling or transferring the design onto the fabric in advance can be helpful when attempting to draw complex designs. Fabric paint that is opaque can completely cover any marks placed on the shirt, but slightly transparent paint will allow markings to show through.

